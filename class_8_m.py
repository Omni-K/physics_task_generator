def prog_dmitrieva():
    """
    Дмитриева Ксения
    :return:
    """
    import random
    t1 = random.randint(600, 800)
    m1 = random.randint(1, 10)
    l = int(340000)
    c = int(460)
    t = int(t1 - 0)
    text = f'''Решите задачу. Железная заготовка, охлаждаясь от температуры {t1}°С до 0°С, растопила лед массой {m1} кг, взятый при 0°С. Какова масса заготовки, если вся энергия, выделенная ею, пошла на плавление льда? В ответ запишите только число, без единиц измерения, округлив его до целого.
    Табличные данные:
     ● Удельная теплоемкость железа - 460 Дж / кг * °С
     ● Удельная теплота плавления льда - 34 * 10^4 Дж / кг '''
    correct = (m1 * l) / (t * c)
    correct = round(correct)
    print(text)
    answer = float(input("Введите ответ: "))
    if correct == answer:
        print("Ответ верный!")
    else:
        print(f'''"Ответ неверный! Правильный ответ: {correct}"''')


