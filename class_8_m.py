def prog_gavr():
    """
    Решение Гавриловой Анастасии
    """
    import random
    f = random.randint(10, 20)
    s = random.randint(2, 5)
    text = f''' Какое давление оказывает острие шила, если сила давления равна {f}, а площадь острия равна {s}?'''
    correct = f / s

    answer = input('Введите ответ:')

    if correct == answer:
        print('Правильно')
    else:
        print(f'Неверно. Верный ответ {correct}')


